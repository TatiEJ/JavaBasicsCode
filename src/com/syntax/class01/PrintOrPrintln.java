package com.syntax.class01;

public class PrintOrPrintln {

	public static void main(String[] args) {
		// see the difference between println vs print
		
		System.out.println("hello");
		System.out.println("Guapo de mi vida");
		System.out.println("I love you");
		
		//
		/* 
		 * the difference between println and print is
		 * println run in different lines
		 * print run in just a line 
		 */
		
		
		
		System.out.print("hello");
		System.out.print(" Guapo de mi vida");
		System.out.print(" I love you");

	}

}
