package com.syntax.class08;

public class task08 {

	public static void main(String[] args) {
		// 1 to 100
		for(int i=1; i<=100; i++) {
		System.out.print(i+ " ");
	}
		
		//100 to 0
		for(int n=100; n>=1; n--) {
			System.out.println(n+ " ");
	}
		
		//even numbers from 20 to 1 (2 ways)
		
		for(int t=20; t>=1; t-=2) {
			System.out.println(t);
	}
		
		
	}
}
