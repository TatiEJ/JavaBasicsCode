package com.syntax.class08;

public class FoorLoopExamples {

	public static void main(String[] args) {
		// i nned to print numbers from 1 to 90

		for(int i = 1; i <= 90; i++) {
			System.out.print(i + " ");
		}
		
		// i need numbers from 60 to 10  
	
		for(int a = 60; a >= 10; a--) {
			System.out.print(a + " ");
	}
		
		
	}

}
